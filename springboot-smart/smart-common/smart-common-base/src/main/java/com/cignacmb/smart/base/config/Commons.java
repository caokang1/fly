package com.cignacmb.smart.base.config;

/**
 * 该功能类来存放固定字段，避免在每个类中出现具体的字段值
 * 命名方式如：大类_名称
 * @author r9wuxx
 */
public class Commons {

    /**
     * session标记，前端直接下载类型的功能来判断下载是否完成，然后根据该标记取消遮罩
     */
    public static final String SESSION_DOWNLOADFLAG = "downloadflag";

    /**
     * session标记，用户信息在session中的key
     */
    public static final String SESSION_USERINFO = "userInfo";

    /**
     * session标记，GI在session中的可以
     */
    public static final String SESSION_GI = "GI";

    /**
     * session标记，session过期时间，秒
     */
    public static final int SESSION_MaxInactiveInterval = 1800;

    /**
     * 系统批处理默认执行用户名称
     */
    public static final String BATCH_OPERATOR = "batch";

    /**
     * quartz写入JobDataMap时默认使用的key
     */
    public static final String QUARTZ_JOB_ENTITY = "entity";

}
