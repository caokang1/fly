package com.cignacmb.smart.base.common;

import com.cignacmb.smart.entity.base.Lduser;

public class UserInfo extends Lduser {
    /**
     * 多机构数据权限
     */
    private String comcodes;
    /**
     * 登陆时选择的管理机构
     */
    private String loginManagecom;
    /**
     * 渠道
     */
    private String branchtype;

    public String getComcodes() {
        return comcodes;
    }

    public void setComcodes(String comcodes) {
        this.comcodes = comcodes;
    }

    public String getLoginManagecom() {
        return loginManagecom;
    }

    public void setLoginManagecom(String loginManagecom) {
        this.loginManagecom = loginManagecom;
    }

    public String getBranchtype() {
        return branchtype;
    }

    public void setBranchtype(String branchtype) {
        this.branchtype = branchtype;
    }
}
